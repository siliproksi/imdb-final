export function getCountries(locale = 'en') {
  return Object.keys(countriesData).map(countryKey => ({
    value: countryKey,
    label: countriesData[countryKey][locale]
  }))
}

export function getCities(countryKey, locale = 'en') {
  if (!countryKey || !countriesData[countryKey]) {
    return []
  }
  
  return Object.keys(countriesData[countryKey].cities).map(cityKey => ({
    value: cityKey,
    label: countriesData[countryKey].cities[cityKey][locale]
  }))
}

export function getCountryLabel(countryKey, locale = 'en') {
  if (!countryKey || !countriesData[countryKey]) {
    return countryKey
  }
  return countriesData[countryKey][locale]
}

export function getCityLabel(countryKey, cityKey, locale = 'en') {
  if (!countryKey || !cityKey || !countriesData[countryKey] || !countriesData[countryKey].cities[cityKey]) {
    return cityKey
  }
  return countriesData[countryKey].cities[cityKey][locale]
}